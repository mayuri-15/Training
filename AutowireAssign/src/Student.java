

public class Student {
	
	private String sname;
	private int sage;
	
	public Student(){
		super();
	}
	public Student(String sname,int sage){
		super();
		this.sname=sname;
		this.sage=sage;
	}
	public String getSname() {
		return sname;
	}
	public void setSname(String sname) {
		this.sname = sname;
	}
	public int getSage() {
		return sage;
	}
	public void setSage(int sage) {
		this.sage = sage;
	}
	

}

